	<h2>About</h2>
	<p>
		This project aims to challenge the concept of “settlement” in Lawrence, Kansas by taking
		seriously the myriad of experiences of unhoused, encamped, or precariously housed people who
		have lived and built in Lawrence over time. This map is one part of the project “Unsettled
		Lawrence” which includes historical archival sources, ethnography, participant photography, and
		oral histories.
	</p>
	<p>
		These 54 data points track approximate location and language used to describe specific spaces by
		eleven different Lawrence newspapers between 1856-1922.
	</p>
	<p>You can adjust two things on this map independently of one another:</p>
	<p>
		First, you can adjust the Sanborn Maps by year. Sanborn Maps were fire insurance maps drawn by
		the Sanborn Map Company. These maps use color-coding and lines to indicate what a building was,
		what it was made from, how many stories it had, and so forth. For more on how to read the
		Sanborn map see here. Maps were made periodically over time. In this map, you can filter by the
		1883, 1887, 1889, or 1905 maps.
	</p>
	<p>
		Second, you can adjust the beginning start date and the ending date in order to see how these
		spaces changed over time.
	</p>
	<p>
		This is a map of language as much as it is a map of space. You can click on each point and see
		the derogatory and racialized language used to describe the space or the person found in this
		particular area.
	</p>
	<p>The areas mapped were commonly used by newspapers in Lawrence between 1856-1922.</p>
	<p>
		<b>East Bottoms:</b> The general term for the area of Lawrence east of Massachusetts street. It contained
		a number of illegal economies including joints (sold beer and liquor) and sex work, home to criminals.
		For more information about “Bottoms” see analysis.
	</p>
	<p>
		<b>Poverty Flat:</b> also called Buzzard’s Glen, this was an area to the west of North Lawrence along
		the river. First mentioned in 1888, newspapers note that this particular locations contained sex
		workers, a “headquarters for burglars and night robbers.” The term “poverty flat” comes from a ballad
		written by writer and poet Brette Harte.
	</p>
	<p>
		<b>Bad Lands:</b> First used in 1892, the Bad Lands may have been the original name of the East Bottoms.
		However, it soon morphs into a location within the larger East Bottoms. Joints, criminal activities,
		and violence occur in this area.
	</p>
	<p>
		<b>Smokey Pilgrims:</b> a migrant family, believed to be the most uneducated and depraved in Lawrence.
		A mixed-race family, famed sociologist F W Blackmar taught classes about this family to his sociology
		and criminal pathology classes and published on them in the American Journal of Sociology. The children
		were well-known to live between houses of the different family members, to wander the streets, be
		prone to idleness, petty theft, and eventually arson.
	</p>
	<p>
		<b>Other Locations:</b> indicates spaces where hobo(e)s or transients often hung out or were arrested.
	</p>
