	<h2>Methodology</h2>
	<p>
		Each of the 54 data points indicates a location in Lawrence considered crime-ridden and
		“unsettled,” or having “unsettled” people associated with them, according to eleven Lawrence
		newspapers between 1873-1922. Chosen from a list of 300 separate mentions of unsettled
		locations, these 54 points were most easily mapped for this prototype.
	</p>
	<p>In order to track a discourse of “unsettlement”:</p>
	<ol>
		<li>
			I looked specifically for language describing a built environment, or a type of person,
			that/who was considered temporary, inappropriate for the time and place, and/or impoverished.
			These are all qualities associated with unsettled-ness. These words included, for example:
			hovel, shack, shantytown, dirty, “hobo” or hoboes,” transient, destitute, rounder, tramp, and
			so forth. This key word search began in 2023.
		</li>
		<li>
			As I looked for these words, I began to see that this language was limited to specific areas
			within Lawrence or reserved for particular people in Lawrence. I adjusted my searches,
			following particular location names (e.g. like Buzzard’s Glen), particular family names (such
			as Smokey Pilgrims), and particular individual people (such as Milton “Hog” Woodruff). I kept
			a database of the location name or how a dwelling was described, racial information given, the
			language used to describe people, as well as location title and approximate location area
			across different newspapers. This resulted in over 300 data points found in over 15 Lawrence
			newspapers between 1856-1922.
		</li>
		<li>
			I chose 54 of the over 300 points representing place names or spaces where certain people
			considered “unsettled” were known to hang out. The specific space names reflected here are:
			East Bottoms, Poverty Flat/Buzzard’s Glen, Bad Lands, and Smokey Pilgrims. There were other
			titles for spaces (like Smoker’s Row or Greasy Hollow), but these four came up most
			consistently. The 54 points represent some of the most easily identified spaces. Because these
			areas were not “official” locations, I often had to determine location or the general block by
			referential data only. These points are only approximations of where an event, a person, or a
			location was. It is not meant to be exact (please see analysis).
		</li>
	</ol>
